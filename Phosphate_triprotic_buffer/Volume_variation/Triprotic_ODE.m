function dy = Triprotic_ODE(t,y,K)
dy = zeros(8,1);    % a column vector
dy(1) = -K(1)*y(1)+K(2)*y(2)*y(7);
dy(2) = K(1)*y(1)-K(2)*y(2)*y(7)-K(3)*y(2)+K(4)*y(3)*y(7)+K(7)*y(5);
dy(3) = K(3)*y(2)-K(4)*y(3)*y(7)-K(5)*y(3)+K(6)*y(4)*y(7)+K(8)*y(6);
dy(4) = K(5)*y(3)-K(6)*y(4)*y(7);
dy(5) = -K(7)*y(5);
dy(6) = -K(8)*y(6);
dy(7) = K(1)*y(1)-K(2)*y(2)*y(7)+K(3)*y(2)-K(4)*y(3)*y(7)+K(5)*y(3)-K(6)*y(4)*y(7)+K(9)-K(10)*y(7)*y(8);
dy(8) = K(9)-K(10)*y(7)*y(8);


